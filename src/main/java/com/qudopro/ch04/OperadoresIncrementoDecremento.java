package com.qudopro.ch04;

public class OperadoresIncrementoDecremento {
    public static void main(String[] args){
        int x = 5;
        int y = 5;

        System.out.println("x++: " + x++);
        System.out.println("y++: " + y++);

        System.out.println("x: " + x);
        System.out.println("y: " + y);

        System.out.println("++x: " + ++x);
        System.out.println("++y: " + ++y);

        System.out.println("x: " + x);
        System.out.println("y: " + y);

        System.out.println("x--: " + x--);
        System.out.println("y--: " + y--);

        System.out.println("x: " + x);
        System.out.println("y: " + y);

        System.out.println("--x: " + --x);
        System.out.println("--y: " + --y);

        System.out.println("x: " + x);
        System.out.println("y: " + y);


    }

}
